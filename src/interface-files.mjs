/**
 * interface-files — project-local agent interface shims.
 *
 * Thin, GENERATED pointers installed into a consuming repo so every agent
 * runtime (.codex / .claude / .agents) can trigger the workflow. The real law
 * lives in the engine's skills/; these shims carry an engineVersion marker so
 * `htw doctor` can detect stale shim text exactly like config drift (the 0.3.4
 * and 0.3.5 skill laws shipped to zero consumers whose shims predated them).
 *
 * Node ESM, built-ins only.
 */
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { commandSignaturesMarkdown } from "./command-specs.mjs";

const PACKAGE_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");

function engineVersion() {
  try {
    return JSON.parse(readFileSync(join(PACKAGE_ROOT, "package.json"), "utf8")).version || "0.0.0";
  } catch {
    return "0.0.0";
  }
}

