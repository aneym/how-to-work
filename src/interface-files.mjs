function targets(v) {
  const runtimes = [".codex", ".claude", ".agents"];
  const out = [];
  for (const rt of runtimes) {
    out.push([join(rt, "skills", "htw", "SKILL.md"), HTW_SKILL(v)]);
    out.push([join(rt, "skills", "htw-doctor", "SKILL.md"), HTW_DOCTOR_SKILL(v)]);
    out.push([join(rt, "skills", "how-to-work", "SKILL.md"), LEGACY_SKILL(v)]);
    out.push([join(rt, "commands", "htw.md"), HTW_COMMAND(v)]);
    out.push([join(rt, "commands", "htw-doctor.md"), HTW_DOCTOR_COMMAND(v)]);
    out.push([join(rt, "commands", "how-to-work.md"), LEGACY_COMMAND(v)]);
    out.push([join(rt, "commands", "how-to.md"), LEGACY_COMMAND(v)]);
  }
  return out;
}

export function installAgentInterfaces(root, { force = false } = {}) {
  const v = engineVersion();
  const written = [];
  const skipped = [];
  for (const [rel, body] of targets(v)) {
    const abs = join(root, rel);
    const content = body.endsWith("\n") ? body : `${body}\n`;
    if (existsSync(abs) && !force) {
      // Same-version shims are current; only silently skip those. Anything
      // else is stale and needs --force (doctor reports it).
      skipped.push(rel);
      continue;
    }
    mkdirSync(dirname(abs), { recursive: true });
    writeFileSync(abs, content, "utf8");
    written.push(rel);
  }
  return { written, skipped };
}

/** Audit shims without writing: which are missing, which are stale (wrong version/content). */
export function auditAgentInterfaces(root) {
  const v = engineVersion();
  const missing = [];
  const stale = [];
  for (const [rel, body] of targets(v)) {
    const abs = join(root, rel);
    if (!existsSync(abs)) {
      missing.push(rel);
      continue;
    }
    const current = readFileSync(abs, "utf8");
    const expected = body.endsWith("\n") ? body : `${body}\n`;
    if (current !== expected) stale.push(rel);
  }
  return { missing, stale, engineVersion: v };
}
